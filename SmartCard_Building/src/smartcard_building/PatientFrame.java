    private void btnAddPatientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPatientActionPerformed
        // TODO add your handling code here:
        
        String pin = Arrays.toString(txt_input_PIN.getPassword());
        String cf_pin = Arrays.toString(txt_confirm_PIN.getPassword());
        if ((pin.equals(cf_pin)) == false) {
            JOptionPane.showMessageDialog(null, "Mã PIN xác nhận không trùng khớp.");
        } else {
            if (pin.length() != 18) {
                JOptionPane.showMessageDialog(null, "Mã PIN phải có độ dài 6 kí tự");
            } else {
                
                String hoten = txt_input_PatientName.getText();
                String ngaysinh = txt_input_PatientDOB.getText();
                String quequan = txt_input_PatientAddress.getText();
                String gioitinh = txt_input_PatientGender.getText();
                String soBHYT = txt_input_InsuranceCode.getText();
                String mabenhnhan = txt_input_PatientCode.getText();
                
//                System.out.println("pin: " + pin + "//hoten: " + hoten + "//ngaysinh: " + ngaysinh +"//quequan: " + quequan + 
//                        "//gioitinh: " + gioitinh + "//mabenhnhan: " + mabenhnhan);
                
                benhnhan.setMaPIN(new String(pin));
                benhnhan.setMaBenhNhan(mabenhnhan);
                benhnhan.setHoten(hoten);
                benhnhan.setGioitinh(gioitinh);
                benhnhan.setSoBHYT(soBHYT);
                benhnhan.setNgaysinh(ngaysinh);
                benhnhan.setQuequan(quequan);
                
                benhnhan.setSoDu(0);
                
                JOptionPane.showMessageDialog(null, "Thành công. Trở lại màn hình chính, [Send Data] để thêm dữ liệu vào thẻ!");
                
                txt_input_PIN.setText("");
                txt_confirm_PIN.setText("");
                txt_input_PatientGender.setText("");
                txt_input_PatientName.setText("");
                txt_input_PatientCode.setText("");
                txt_input_PatientDOB.setText("");
                txt_input_InsuranceCode.setText("");
                txt_input_PatientAddress.setText("");
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnAddPatientActionPerformed

