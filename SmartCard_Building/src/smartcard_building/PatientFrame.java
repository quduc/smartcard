    private void btnImageSelectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageSelectionActionPerformed
        // TODO add your handling code here:
        
        //https://www.programcreek.com/2009/02/java-convert-image-to-byte-array-convert-byte-array-to-image/
        //https://www.youtube.com/watch?v=p4HV2zDcANI&ab_channel=MauriceMuteti
        
        JFileChooser browserImage = new JFileChooser();
        // filter image extensions
        FileNameExtensionFilter fnef = new FileNameExtensionFilter("IMAGES", "png", "jpg", "jpeg");
        browserImage.addChoosableFileFilter(fnef);
        
        int showOpenDialog = browserImage.showOpenDialog(null);
        
        if(showOpenDialog == JFileChooser.APPROVE_OPTION) {
            File selectedFileImage = browserImage.getSelectedFile();
            String selectedFilePath = selectedFileImage.getAbsolutePath();
            JOptionPane.showMessageDialog(null, selectedFilePath );
            
            // display image on JLabel
            ImageIcon ii = new ImageIcon(selectedFilePath);
            // resize image to fit the jlabel
            int width = input_PatientImage.getWidth();
            int height = input_PatientImage.getHeight();
            Image image = ii.getImage().getScaledInstance(width, height, Image.SCALE_SMOOTH);
            
            input_PatientImage.setIcon(new ImageIcon(image));
        }
        
        
    }//GEN-LAST:event_btnImageSelectionActionPerformed
                        

    
